package lezione1_Intro;

public class Tipi {
	
	public static void main(String[] args) {
		//Carrellata sui tipi
		
		String nome = "Dario"; //ATT: String non appartiene ai tipi primitivi

		char lettera = 'D';
		
		///////// numerici interi
		
		byte anni = 36; // 8 bit
		short litri = 398; // 16 bit
		int denari = 1500; // 32bit
		long etaTerra = 4000000000L; //64 bit

		//////////tipi Reali, numerici con virgola
		float piGreco = 3.145f;
		double nepero = 2.71;
		
		///tipi Boolean (solitamente 1 bit)
		boolean v = true;
		boolean f = false;

		
	}

}
